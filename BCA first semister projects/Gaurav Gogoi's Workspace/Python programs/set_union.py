# The methods or functions which are used to perform union and intersection of sets like in mathematics are
# 1. set.union(set2)
# 2. set.intersection(set2)
number1={1,2,3,4}
number2={3,4,5,6,7,8,9,10}

unified_set=number1.union(number2)
print(unified_set)
inter_set=number1.intersection(number2)
print(inter_set)
