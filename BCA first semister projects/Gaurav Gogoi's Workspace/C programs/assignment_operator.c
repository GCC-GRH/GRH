#include<stdio.h>
void main(){
    /*This program teaches how to use the assignment and shorthand assignment operators*/
int i=10,j=10,k=10,l=10,m=10; //USE OF ASSIGNMENT OPERATOR = FOR ASSIGINING A VALUE TO A TYPE OF VARIABLE

//SHORTHAND ASSIGNMENT OPERATORS
i+=10;
j-=10;
k*=10;
l/=10;
m%=10;
printf("\n The value of i is %d:",i);
printf("\n The value of j is %d:",j);
printf("\n The value of k is %d:",k);
printf("\n The value of l is %d:",l);
printf("\n The value of m is %d:",m);
}